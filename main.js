/*
    getGrid(gridSpacing, subDivide)
    get the markup to render a noice grid yo
*/
getGrid(gridSpacing, subDivide){
    let tmpHtml = ['<g class="bkndGrid" opacity="0">'];
    let cols = Math.floor(window.innerWidth / gridSpacing);
    let rows = Math.floor(window.innerHeight / gridSpacing);

    //cols
    for (let i=0; i < ((window.innerWidth / cols)/2); i++){
        let lineWeight = (i%subDivide == 0)?.6:.2;
        if (i == 0){ lineWeight = 1; }
        tmpHtml.push(`<line x1="${cols * i}" y1="${window.innerHeight/-2}" x2="${cols * i}" y2="${window.innerHeight}" stroke="rgba(240, 240, 240, ${lineWeight})" stroke-width="1" />`);
        if (i > 0){ tmpHtml.push(`<line x1="${cols * -i}" y1="${window.innerHeight/-2}" x2="${cols * -i}" y2="${window.innerHeight}" stroke="rgba(240, 240, 240, ${lineWeight})" stroke-width="1" />`); }
    }
    //rows
    for (let i=0; i < ((window.innerHeight / rows)/2); i++){
        let lineWeight = (i%subDivide == 0)?.6:.2;
        if (i == 0){ lineWeight = 1; }
        tmpHtml.push(`<line y1="${cols * i}" x1="${window.innerWidth/-2}" y2="${cols * i}" x2="${window.innerWidth}" stroke="rgba(240, 240, 240, ${lineWeight})" stroke-width="1" />`);
        if (i > 0){ tmpHtml.push(`<line y1="${cols * -i}" x1="${window.innerWidth/-2}" y2="${cols * -i}" x2="${window.innerWidth}" stroke="rgba(240, 240, 240, ${lineWeight})" stroke-width="1" />`); }
    }
    tmpHtml.push('</g>');
    return(tmpHtml.join(''));
}




/*
    mySetFocus(self)
    the UI has changed focus, this is a hardcode focusCallback
*/
async mySetFocus(focus){

    // execute setup only once
    if (this.firstFocus){
        try {
            this.setup();
        }catch(e){
            console.log(e);
        }

        this.firstFocus = false;
    }

    if (focus && (! this.focus)){
        /* gaining focus from a non-focussed state */
        this.gainFocus();
    }else if ((! focus) && this.focus){
        /* losing focus from a focussed state */
        this.leaveFocus();
    }
}




